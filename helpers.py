def celcius_to_kelvin(celcius_temperature):
    # function to convert celcius to kelvin
    # formula: c + 273.15
    kelvin_temperature = celcius_temperature + 273.15
    return kelvin_temperature


def celcius_to_fahrenheit(temperature_celcius):
    # formula = (C * 1.8) + 32
    temperature_fahrenheit = temperature_celcius * 1.8 + 32
    return temperature_fahrenheit

